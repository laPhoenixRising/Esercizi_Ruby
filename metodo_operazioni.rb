# definire 4 metodi, uno per ogni operazione matematica, che accettano due numeri e restituiscono il risultato dell'operazione

def somma(a, b)
    s = a + b
    return s
end

def sottrazione(a, b)
    s = a - b
    return s
end

def moltiplicazione(a, b)
    m = a * b
    return m
end

def divisione(a, b)
    d = a / b
    return d
end    

